public class Operators {
    public static void main(String [] args){
        // different operators
        int a = 2;
        // left shift
        // moves the bits to left by one.
        System.out.println("Left shift " + (a<<2));
        // right shift
        // moves the bits to right by one.
        System.out.println("Right shift "+ (a>>2));
    }
}